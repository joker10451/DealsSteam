async def post_tip_of_the_week():
    """Публикует случайный совет, который ещё не был опубликован."""
    bot = get_bot()
    if not bot:
        log.warning("tips: бот не инициализирован")
        return

    # Фильтруем уже опубликованные
    available = []
    for tip_id, text in TIPS:
        if not await is_already_posted(tip_id):
            available.append((tip_id, text))

    if not available:
        # Все советы показаны — сбрасываем цикл (начинаем заново)
        log.info("tips: все советы показаны, начинаем новый цикл")
        available = TIPS

    tip_id, text = random.choice(available)

    keyboard = InlineKeyboardMarkup(inline_keyboard=[
        [
            InlineKeyboardButton(text="👍 Полезно", callback_data=f"tip_useful:{tip_id}"),
            InlineKeyboardButton(text="➕ Вишлист", callback_data="wl_add_prompt"),
        ]
    ])

    try:
        await send_with_retry(lambda: bot.send_message(
            CHANNEL_ID,
            text,
            reply_markup=keyboard,
            disable_web_page_preview=True,
        ))
        await mark_as_posted(tip_id, f"Совет: {tip_id}", "tip", 0, "")
        log.info(f"tips: опубликован совет {tip_id}")
    except Exception as e:
        log.error(f"tips: ошибка публикации {tip_id}: {e}")
